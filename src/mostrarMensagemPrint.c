#include <stdio.h>

int main() {
  printf("ola, tenho 21 anos.\n");
  printf("valor inteiro: %d.\n", 10);
  printf("valor real: %f.\n", 3.14159265);
  printf("valor real com duas casas: %.2f.\n", 3.14159265);
  printf("dado de texto: %c.\n", 'a');
  printf("dado de texto: %s.\n", "testando");
}